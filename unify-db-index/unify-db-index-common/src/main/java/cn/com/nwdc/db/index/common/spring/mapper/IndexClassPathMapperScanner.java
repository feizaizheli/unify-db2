    public IndexClassPathMapperScanner(BeanDefinitionRegistry registry) {
        super(registry, false);

    }


    public void setMapperFactoryBean(IndexMapperFactoryBean<?> mapperFactoryBean) {
        this.mapperFactoryBean = mapperFactoryBean != null ? mapperFactoryBean : new IndexMapperFactoryBean<Object>();
    }


